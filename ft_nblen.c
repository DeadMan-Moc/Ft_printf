#include "ft_printf.h"

int		ft_nblen(int nb)
{
	int	digit;

	digit = 0;
	if (!nb)
		return (1);
	if (nb < 0)
		digit++;
	while (nb != 0)
	{
		digit++;
		nb = nb / 10;
	}
	return (digit);
}
