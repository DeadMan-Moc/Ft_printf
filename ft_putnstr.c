#include "ft_printf.h"

void	ft_putnstr(char *s, int n)
{
	int i;

	if (!s)
		return ;
	i = 0;
	while (i < n && s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}
